(function() {
	
	'use strict';
	
	let	utils	= {};
	
	window[ 'utils' ]	= utils;
	
	/*
	 * [
	 * 	[ 0, 1, 2 ] // 0,n
	 * 	[ 0, 1, 2 ] // 1,n
	 * 	[ 0, 1, 2 ] // 2,n
	 * ]
	 */
	function multMatrices3x3( a, b ) {
		
		return	[
			[
				( a[ 0 ][ 0 ] * b[ 0 ][ 0 ] )
				+ ( a[ 0 ][ 1 ] * b[ 1 ][ 0 ] )
				+ ( a[ 0 ][ 2 ] * b[ 2 ][ 0 ] ),
				
				( a[ 0 ][ 0 ] * b[ 0 ][ 1 ] )
				+ ( a[ 0 ][ 1 ] * b[ 1 ][ 1 ] )
				+ ( a[ 0 ][ 2 ] * b[ 2 ][ 1 ] ),
				
				( a[ 0 ][ 0 ] * b[ 0 ][ 2 ] )
				+ ( a[ 0 ][ 1 ] * b[ 1 ][ 2 ] )
				+ ( a[ 0 ][ 2 ] * b[ 2 ][ 2 ] )
			],
			[
				( a[ 1 ][ 0 ] * b[ 0 ][ 0 ] )
				+ ( a[ 1 ][ 1 ] * b[ 1 ][ 0 ] )
				+ ( a[ 1 ][ 2 ] * b[ 2 ][ 0 ] ),
				
				( a[ 1 ][ 0 ] * b[ 0 ][ 1 ] )
				+ ( a[ 1 ][ 1 ] * b[ 1 ][ 1 ] )
				+ ( a[ 1 ][ 2 ] * b[ 2 ][ 1 ] ),
				
				( a[ 1 ][ 0 ] * b[ 0 ][ 2 ] )
				+ ( a[ 1 ][ 1 ] * b[ 1 ][ 2 ] )
				+ ( a[ 1 ][ 2 ] * b[ 2 ][ 2 ] )
			],
			[
				( a[ 2 ][ 0 ] * b[ 0 ][ 0 ] )
				+ ( a[ 2 ][ 1 ] * b[ 1 ][ 0 ] )
				+ ( a[ 2 ][ 2 ] * b[ 2 ][ 0 ] ),
				
				( a[ 2 ][ 0 ] * b[ 0 ][ 1 ] )
				+ ( a[ 2 ][ 1 ] * b[ 1 ][ 1 ] )
				+ ( a[ 2 ][ 2 ] * b[ 2 ][ 1 ] ),
				
				( a[ 2 ][ 0 ] * b[ 0 ][ 2 ] )
				+ ( a[ 2 ][ 1 ] * b[ 1 ][ 2 ] )
				+ ( a[ 2 ][ 2 ] * b[ 2 ][ 2 ] )
			]
		];
		
	}
	
	utils[ 'multMatrices3x3' ]	= multMatrices3x3;
	
})();
